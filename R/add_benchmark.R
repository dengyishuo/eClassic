#' Add Benchmark Returns to Data
#'
#' Extract returns of a benchmark index (e.g., CSI300) from the data and add
#' them as a new column to every row. This facilitates rolling beta calculations
#' against a market benchmark.
#'
#' @param data A data.frame or tibble in long format (required columns: `date`,
#'   `code`, and a return column for the benchmark). Typically obtained via
#'   `get_data()` and then processed with `add_return()`.
#' @param market_code Character. The stock code of the benchmark index as it
#'   appears in the `code` column (e.g., `"000300.SS"` for CSI300).
#' @param y_col Character. Name of the return column to use for the benchmark.
#'   Default is `"ret_1"`.
#' @param new_col Character. Name of the new column to store the benchmark
#'   returns. Default is `"benchmark_ret"`.
#' @param keep_benchmark Logical. If `TRUE`, keep rows where `code == market_code`
#'   in the output; if `FALSE`, filter out those rows. Default is `FALSE` because
#'   benchmark rows are usually not needed for factor analysis.
#' @param output Character. Either `"tibble"` (default) or `"data.frame"`.
#'
#' @return A data frame or tibble with an additional column containing
#'   benchmark returns. If `keep_benchmark = FALSE`, benchmark rows are removed.
#'
#' @details
#' The function assumes that the input `data` already contains daily returns
#' (e.g., generated by `add_return()`) for both stocks and the benchmark index.
#' It extracts the benchmark return series by `date`, replicates it across all
#' stocks, and optionally discards the benchmark rows.
#'
#' @seealso \code{\link{add_return}}, \code{\link{add_beta}}
#'
#' @export
#'
#' @importFrom dplyr filter select left_join rename
#' @importFrom rlang :=
#' @importFrom tibble as_tibble
#'
#' @examples
#' \dontrun{
#' library(FactorCraft)
#' library(dplyr)
#'
#' stock_df <- data.frame(
#'   code = c("000001.SZ", "000002.SZ", "000300.SS"),
#'   name = c("Ping An Bank", "Vanke A", "CSI300")
#' )
#'
#' dat <- get_data(stock_df, start = "2024-01-01", end = "2024-12-31") |>
#'   add_return(n = 1, type = "discrete")
#'
#' dat_with_bench <- add_benchmark(dat, market_code = "000300.SS")
#'
#' head(dat_with_bench)
#' }
add_benchmark <- function(data,
                          market_code,
                          y_col = "ret_1",
                          new_col = "benchmark_ret",
                          keep_benchmark = FALSE,
                          output = c("tibble", "data.frame")) {
  output <- match.arg(output)

  # Check if market_code exists in data
  if (!market_code %in% data$code) {
    stop("market_code '", market_code, "' not found in data$code")
  }

  # Check if y_col exists
  if (!y_col %in% colnames(data)) {
    stop("Column '", y_col, "' not found in data. Did you run add_return() first?")
  }

  # Extract benchmark returns
  bench_data <- data %>%
    dplyr::filter(code == market_code) %>%
    dplyr::select(date, .bench_ret = dplyr::all_of(y_col))

  # Merge benchmark returns to all rows
  res <- data %>%
    dplyr::left_join(bench_data, by = "date") %>%
    dplyr::rename(!!new_col := .bench_ret)

  # Optionally remove benchmark rows
  if (!keep_benchmark) {
    res <- res %>% dplyr::filter(code != market_code)
  }

  # Output format
  if (output == "data.frame") {
    res <- as.data.frame(res, stringsAsFactors = FALSE)
  } else {
    res <- tibble::as_tibble(res)
  }

  return(res)
}
